void make_crc8_table(void) {
    for (size_t idx = 0; idx < (1 << CRC8_WIDTH); idx++) {
        uint8_t crc8 = idx;
        for (uint8_t bit = 0; bit < CRC8_WIDTH; bit++) {
            if ((crc8 & (1 << (CRC8_WIDTH - 1))) != 0) {
                crc8 = (crc8 << 1) ^ CRC8_POLY;
            } else {
                crc8 <<= 1;
            }
        }
        crc8_table[idx] = crc8;
        printf("0x%02x, ", crc8_table[idx]);
        if ((idx + 1) % 16 == 0) {
            printf("\n");
        }
    }
}

void producer(void *arg) {
    FILE *file = fopen(arg, "rb");
    if (file == NULL) {
        printf("Failed to open file\n");
        return;
    }

    while (true) {
        mtx_lock(&mutex);
        while (count == BUFFER_SIZE) {
            cnd_wait(&can_produce, &mutex);
        }

        count = fread(buffer, 1, BUFFER_SIZE, file);
        if (count < BUFFER_SIZE) {
            producer_done = true;
        }
        cnd_signal(&can_consume);
        mtx_unlock(&mutex);

        if (producer_done) {
            break;
        }
    }

    fclose(file);
}

void consumer(void *arg) {
    while (1) {
        mtx_lock(&mutex);
        while (count == 0 && producer_done == false) {
            cnd_wait(&can_consume, &mutex);
        }

        if (count == 0 && producer_done == true) {
            break;
        }


        for (size_t idx = 0; idx < count; ++idx) {
            crc = crc8_table[crc ^ buffer[idx]];
        }
        count = 0;
        cnd_signal(&can_produce);
        mtx_unlock(&mutex);
    }
}

int main(int argc, char *argv[]) {
    clock_t start = clock();
    buffer = malloc(BUFFER_SIZE);
    thrd_t producer_thread, consumer_thread;
    mtx_init(&mutex, mtx_plain);
    cnd_init(&can_produce);
    cnd_init(&can_consume);

    thrd_create(&producer_thread, (thrd_start_t) producer, argv[1]);
    thrd_create(&consumer_thread, (thrd_start_t) consumer, NULL);

    thrd_join(producer_thread, NULL);
    thrd_join(consumer_thread, NULL);

    mtx_destroy(&mutex);
    cnd_destroy(&can_produce);
    cnd_destroy(&can_consume);
    free(buffer);

    printf("CRC8: 0x%02x\n", crc);
    clock_t end = clock();
    double duration = (double)(end - start) / CLOCKS_PER_SEC;
    printf("cost: %.3f s\n", duration);
    return 0;
}
